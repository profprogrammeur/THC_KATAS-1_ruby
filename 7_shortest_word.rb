# Simple, given a string of words, return the length of the shortest word(s).
# String will never be empty and you do not need to account for different data types.

string="Un grand apaisement se fait dans les pauvres esprits fatigués du labeur de la journée"
arr= string.split
min= string.split.min.length
print arr.select{|e|e.length==min}
